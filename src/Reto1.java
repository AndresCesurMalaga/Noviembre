// Escribe un programa en Java que muestre los números del 10 al 1
// Debes utilizar una estructura de tipo while ó do-while.

public class Reto1 {

	public static void main(String[] args) {
		int x;
		x = 10;
		do {
			System.out.print(x + " ");
			x = x - 1;
		} while (x > 0);
	}

}
